import Foundation

struct Math {
    // a
    func addieren(x: Int, y: Int) -> Int {
        return x + y
    }
    
    // s
    func subtrahieren(x: Int, y: Int) -> Int {
        return x - y
    }
    
    // m
    func multiplizieren(x: Int, y: Int) -> Int {
        return x * y
    }
}